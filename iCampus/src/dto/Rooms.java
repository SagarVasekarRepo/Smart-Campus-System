package dto;

public class Rooms {
	public int id;
	
	public String name;
	
	public String room_no;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getRoom_no() {
		return room_no;
	}

	public void setRoom_no(String roomNo) {
		room_no = roomNo;
	} 

}
